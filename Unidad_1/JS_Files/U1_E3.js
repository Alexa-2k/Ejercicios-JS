var numero1 = parseInt(prompt("Ahora vamos a calcular el promedio de cuatro valores de tu elección. Por favor, ingresá el primer número"));
var numero2 = parseInt(prompt("Por favor ingresá el segundo valor"));
var numero3 = parseInt(prompt("Ahora ingresá el tercer valor"));
var numero4 = parseInt(prompt("Por último, ingresá el cuarto valor"));
var suma = (numero1 + numero2 + numero3 + numero4);
var promedio = suma / 4;   
alert("La suma de los cuatro valores que ingresaste es: " + suma+ " y su promedio es: " + promedio);
